def register_helper_tools(mcp):
    @mcp.tool()
    def get_usage_guide():
        """이 MCP 서버의 사용 방법과 가능한 명령어들을 안내합니다."""
        guide = """
# 📅 스마트 일정 및 메모 관리자 사용 가이드

이 서버는 Google Calendar를 사용하여 당신의 일정과 메모를 체계적으로 관리합니다.

### 1. 일정 등록 (카테고리 & 색상)
일정을 등록할 때 아래 3가지 카테고리를 사용할 수 있습니다. 제목에 태그가 붙지 않고 깔끔하게 등록되지만, 캘린더 앱에서는 색상으로 구분됩니다.
- **업무**: 파란색 (예: "오늘 오후 3시 주간 회의 업무로 잡아줘")
- **개인**: 연두색 (예: "내일 저녁 7시 운동 일정 개인으로 등록해줘")
- **경조사**: 빨간색 (예: "이번 주 토요일 친구 결혼식 경조사로 추가해줘")

### 2. 메모 및 세부 정보
모든 일정에는 상세 내용을 적을 수 있습니다. 사용자가 말하는 세부 정보는 구글 캘린더의 '설명(Description)' 칸에 안전하게 저장됩니다.

### 3. 일정 조회
- 기본적으로 **향후 7일**간의 일정을 보여줍니다.
- 사용자가 원하면 **최대 30일**까지 조회 범위를 넓힐 수 있습니다.
- (예: "이번 달 일정 다 보여줘", "향후 2주간 일정 알려줘")

### 4. 긴급 이메일 알림
- 현재 시간 기준, **2시간 이내**의 급박한 일정이 있는지 체크할 수 있습니다.
- 설정된 이메일로 일정 제목과 세부 메모 내용을 함께 발송합니다.
- (예: "곧 시작할 급박한 일정 있으면 내 메일로 보내줘")

궁금한 점이 있으면 언제든 물어보세요!
        """
        return guide